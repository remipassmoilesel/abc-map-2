package org.abcmap.core.configuration;

/**
 * Default names of configuration values
 */
public enum CFNames {

    /**
     * Current language of software
     */
    LANGUAGE,

    /**
     * Default language of software
     */
    DEFAULT_LANGUAGE,

    /**
     * Home directory of user
     */
    HOME,

    /**
     * Title of current progile
     */
    PROFILE_TITLE,

    /**
     * Comment on current profile
     */
    PROFILE_COMMENT,

    /**
     * Real path of current profile
     */
    PROFILE_PATH,

    /**
     * If true, profile will be saved when leave
     */
    SAVE_PROFILE_WHEN_LEAVE,

    IMPORT_ENABLE_CROPPING,

    IMPORT_CROP_AREA_SELECTION_X,

    IMPORT_CROP_AREA_SELECTION_Y,

    IMPORT_CROP_AREA_SELECTION_W,

    IMPORT_CROP_AREA_SELECTION_H,

    /**
     * Delay in milliseconds we have to wait before a window is considered as hidden
     */
    WINDOW_HIDDING_DELAY_MS,

    /**
     * Number of points necessary for assembling tiles
     */
    IMPORT_MATCHING_POINTS_THRESHOLD,

    IMPORT_SURF_MODE,

    ALERT_NOW_IF_REFUSED_TILES,

    DIRECTORY_IMPORT_PATH,

    DOCUMENT_IMPORT_PATH,

    IMPORT_DOCUMENT_SCALE_FACTOR,

    ROBOT_IMPORT_COVERING,

    ROBOT_IMPORT_WIDTH,

    ROBOT_IMPORT_HEIGHT,

    ROBOT_IMPORT_MOVING_DELAY_MS,

    ROBOT_IMPORT_CAPTURE_DELAY_MS,

    ROBOT_IMPORT_MODE,

}